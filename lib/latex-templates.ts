function buildAnswerLatex(answer: Answer): string {
  const solutionLines = sanitizeLatexContent(answer.solution).split("\\n").join("\n\n");
  return `\\textbf{${answer.number}. ${sanitizeLatexContent(answer.correctAnswer)}}

${solutionLines}
`;
}

